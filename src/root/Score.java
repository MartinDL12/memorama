package root;

public class Score {

    private int score;
    private String userName;

    public Score() {
        this.score = score;
        this.userName = userName;
    }

    public Score(int score, String userName) {
        this.score = score;
        this.userName = userName;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public int getScore() {
        return score;
    }

    public String getUserName() {
        return userName;
    }
}
